package com.example.uhlikova_stopa;

public class TravelData {

    private double personalCarPetrolKm;
    private double personalCarDieselKm;
    private double personalCarElectricKm;
    private double busKm;
    private double truckKm;
    private double trainKm;
    private double totalEmissions;


    public double getPersonalCarPetrolKm() {
        return personalCarPetrolKm;
    }

    public void setPersonalCarPetrolKm(double personalCarPetrolKm) {
        this.personalCarPetrolKm = personalCarPetrolKm;
    }

    public double getPersonalCarDieselKm() {
        return personalCarDieselKm;
    }

    public void setPersonalCarDieselKm(double personalCarDieselKm) {
        this.personalCarDieselKm = personalCarDieselKm;
    }

    public double getPersonalCarElectricKm() {
        return personalCarElectricKm;
    }

    public void setPersonalCarElectricKm(double personalCarElectricKm) {
        this.personalCarElectricKm = personalCarElectricKm;
    }

    public double getBusKm() {
        return busKm;
    }

    public void setBusKm(double busKm) {
        this.busKm = busKm;
    }

    public double getTruckKm() {
        return truckKm;
    }

    public void setTruckKm(double truckKm) {
        this.truckKm = truckKm;
    }

    public double getTrainKm() {
        return trainKm;
    }

    public void setTrainKm(double trainKm) {
        this.trainKm = trainKm;
    }

    public double getTotalEmissions() {
        return totalEmissions;
    }

    public void setTotalEmissions(double totalEmissions) {
        this.totalEmissions = totalEmissions;
    }
}
